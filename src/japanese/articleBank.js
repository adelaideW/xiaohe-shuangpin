export function stripAozoraReadings(text) {
  return String(text || '')
    .replace(/([\u4E00-\u9FFF々〆ヵヶ]+)（[ぁ-んーァ-ヶー]+）/g, '$1')
    .replace(/［＃[^\]]*］/g, '')
    .trim()
}

export function estimateJaMinutes(text) {
  const n = [...stripAozoraReadings(text)].filter((ch) => !/\s/.test(ch)).length
  return Math.max(1, Math.min(12, Math.ceil(n / 220)))
}

